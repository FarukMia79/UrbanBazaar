                    items: 1,
                    loop: true,
                    margin: 0,
                    nav: false,
                    dots: true,
                    autoplay: true,
                    smartSpeed: 1000,
                });
            }
        },
        // ২. রিলেটেড প্রোডাক্ট স্লাইডারের জন্য কোড (৫টি আইটেম)
